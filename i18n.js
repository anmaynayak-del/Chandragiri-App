window.normalizeLanguage = function(value) {
    return window.SUPPORTED_LANGUAGES.includes(value) ? value : 'en';
};

window.getStoredLanguage = function() {
    try {
        return window.normalizeLanguage(localStorage.getItem('preferredLanguage') || 'en');
    } catch (_error) {
        return 'en';
    }
};

window.getCurrentLanguage = function() {
    return window.appLanguage;
};

window.t = function(key, vars) {
    const lang = window.getCurrentLanguage();
    const table = window.I18N[lang] || window.I18N.en;
    const fallback = window.I18N.en || {};
    let value = table[key] || fallback[key] || key;
    const replacements = vars || {};
    Object.keys(replacements).forEach(name => {
        value = value.split(`{${name}}`).join(replacements[name]);
    });
    return value;
};

window.getCurrentLocale = function() {
    return window.LANGUAGE_LOCALES[window.getCurrentLanguage()] || window.LANGUAGE_LOCALES.en;
};

window.formatCountValue = function(value) {
    return Number(Number(value || 0).toFixed(2));
};

window.formatLocalizedDayCount = function(value) {
    const num = Number(value || 0);
    const label = Math.abs(num - 1) < 0.001 ? window.t('common.daySingular') : window.t('common.dayPlural');
    return `${window.formatCountValue(num)} ${label}`;
};

window.applyDocumentTranslations = function() {
    document.documentElement.lang = window.getCurrentLanguage();
    document.title = window.t('app.documentTitle');
    const metaDescription = document.querySelector('meta[name="description"]');
    if (metaDescription) metaDescription.setAttribute('content', window.t('app.metaDescription'));
};

window.applyStaticTranslations = function() {
    window.applyDocumentTranslations();

    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = window.t(el.getAttribute('data-i18n'));
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.setAttribute('placeholder', window.t(el.getAttribute('data-i18n-placeholder')));
    });

    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.setAttribute('title', window.t(el.getAttribute('data-i18n-title')));
    });

    document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
        el.setAttribute('aria-label', window.t(el.getAttribute('data-i18n-aria-label')));
    });
};

window.setLanguage = function(language, options) {
    const opts = options || {};
    window.appLanguage = window.normalizeLanguage(language);

    try {
        localStorage.setItem('preferredLanguage', window.appLanguage);
    } catch (_error) {}

    window.applyStaticTranslations();

    const profileLanguage = document.getElementById('profile-language');
    if (profileLanguage) profileLanguage.value = window.appLanguage;

    if (typeof window.onLanguageChanged === 'function') {
        window.onLanguageChanged(window.appLanguage, opts);
    }
};
